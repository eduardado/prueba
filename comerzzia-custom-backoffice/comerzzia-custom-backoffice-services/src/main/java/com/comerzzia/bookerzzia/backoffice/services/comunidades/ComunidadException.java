package com.comerzzia.bookerzzia.backoffice.services.comunidades;


public class ComunidadException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 7371002749254695493L;

	public ComunidadException() {
		super();
	}

	public ComunidadException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public ComunidadException(String message, Throwable cause) {
		super(message, cause);
	}

	public ComunidadException(String message) {
		super(message);
	}

	public ComunidadException(Throwable cause) {
		super(cause);
	}
	
	

}
