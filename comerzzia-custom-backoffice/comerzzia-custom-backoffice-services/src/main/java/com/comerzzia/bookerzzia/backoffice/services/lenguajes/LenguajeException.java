package com.comerzzia.bookerzzia.backoffice.services.lenguajes;


public class LenguajeException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = -3157031972893909572L;

	public LenguajeException() {
		super();
	}

	public LenguajeException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public LenguajeException(String message, Throwable cause) {
		super(message, cause);
	}

	public LenguajeException(String message) {
		super(message);
	}

	public LenguajeException(Throwable cause) {
		super(cause);
	}

}
